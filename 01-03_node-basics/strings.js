var name = 'Corrine';
var lastName = 'Lin';

console.log(name);

// prints both variables, one after the other.
console.log(name + lastName);

// Add *space* string with variables.
console.log(name + ' ' + lastName);

// Length property shows how long a string is. Length property exists for any string variable and returns the number of characters for that string.
console.log(name.length);

// Variable naming. Any alphabet letter can start the name of a variable. Or an underscore or $ sign. Numbers can be used as second character of variable name, but not first character.

// First letter in a variable name is always lowercase, followed by uppercase for second word of a variable name.

var firstName = 'Corrine';
var lastName = 'Lin';

// Concatenate = adding one string to another string
var fullName = firstName + ' ' + lastName;

console.log(fullName);
console.log(fullName.length);
